var (
	Description = utils.FormatUsageDescription("workforcemanagement_notifications_update", "SWAGGER_OVERRIDE_/api/v2/workforcemanagement/notifications/update", )
	workforcemanagement_notifications_updateCmd = &cobra.Command{
		Use:   utils.FormatUsageDescription("workforcemanagement_notifications_update"),
		Short: Description,
		Long:  Description,
	}
	CommandService services.CommandService
)

func init() {
	CommandService = services.NewCommandService(workforcemanagement_notifications_updateCmd)
}

func Cmdworkforcemanagement_notifications_update() *cobra.Command { 
	createCmd.SetUsageTemplate(fmt.Sprintf("%s\nOperation:\n  %s %s\n%s\n%s", createCmd.UsageTemplate(), "POST", "/api/v2/workforcemanagement/notifications/update", utils.FormatPermissions([]string{  }), utils.GenerateDevCentreLink("POST", "Workforce Management", "/api/v2/workforcemanagement/notifications/update")))
	utils.AddFileFlagIfUpsert(createCmd.Flags(), "POST", `{
  "in" : "body",
  "name" : "body",
  "description" : "body",
  "required" : false,
  "schema" : {
    "$ref" : "#/definitions/UpdateNotificationsRequest"
  }
}`)
	
	utils.AddPaginateFlagsIfListingResponse(createCmd.Flags(), "POST", `{
  "description" : "successful operation",
  "schema" : {
    "$ref" : "#/definitions/UpdateNotificationsResponse"
  }
}`)
	workforcemanagement_notifications_updateCmd.AddCommand(createCmd)
	
	return workforcemanagement_notifications_updateCmd
}

var createCmd = &cobra.Command{
	Use:   "create",
	Short: "Mark a list of notifications as read or unread",
	Long:  "Mark a list of notifications as read or unread",
	Args:  utils.DetermineArgs([]string{ }),

	Run: func(cmd *cobra.Command, args []string) {
		_ = models.Entities{}

		printReqBody, _ := cmd.Flags().GetBool("printrequestbody")
		if printReqBody {
			
			reqModel := models.Updatenotificationsrequest{}
			utils.Render(reqModel.String())
			
			return
		}

		queryParams := make(map[string]string)

		path := "/api/v2/workforcemanagement/notifications/update"

		urlString := path
		if len(queryParams) > 0 {
			urlString = fmt.Sprintf("%v?", path)
			for k, v := range queryParams {
				urlString += fmt.Sprintf("%v=%v&", url.QueryEscape(strings.TrimSpace(k)), url.QueryEscape(strings.TrimSpace(v)))
			}
			urlString = strings.TrimSuffix(urlString, "&")
		}

		const opId = "create"
		const httpMethod = "POST"
		retryFunc := CommandService.DetermineAction(httpMethod, urlString, cmd, opId)
		// TODO read from config file
		retryConfig := &retry.RetryConfiguration{
			RetryWaitMin: 5 * time.Second,
			RetryWaitMax: 60 * time.Second,
			RetryMax:     20,
		}
		results, err := retryFunc(retryConfig)
		if err != nil {
			logger.Fatal(err)
		}

		utils.Render(results)
	},
}
