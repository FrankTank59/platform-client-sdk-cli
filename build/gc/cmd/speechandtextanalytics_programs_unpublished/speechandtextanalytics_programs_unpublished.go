var (
	Description = utils.FormatUsageDescription("speechandtextanalytics_programs_unpublished", "SWAGGER_OVERRIDE_/api/v2/speechandtextanalytics/programs/unpublished", )
	speechandtextanalytics_programs_unpublishedCmd = &cobra.Command{
		Use:   utils.FormatUsageDescription("speechandtextanalytics_programs_unpublished"),
		Short: Description,
		Long:  Description,
	}
	CommandService services.CommandService
)

func init() {
	CommandService = services.NewCommandService(speechandtextanalytics_programs_unpublishedCmd)
}

func Cmdspeechandtextanalytics_programs_unpublished() *cobra.Command { 
	utils.AddFlag(listCmd.Flags(), "string", "nextPage", "", "The key for listing the next page")
	utils.AddFlag(listCmd.Flags(), "int", "pageSize", "20", "The page size for the listing")
	listCmd.SetUsageTemplate(fmt.Sprintf("%s\nOperation:\n  %s %s\n%s\n%s", listCmd.UsageTemplate(), "GET", "/api/v2/speechandtextanalytics/programs/unpublished", utils.FormatPermissions([]string{ "speechAndTextAnalytics:program:view",  }), utils.GenerateDevCentreLink("GET", "Speech &amp; Text Analytics", "/api/v2/speechandtextanalytics/programs/unpublished")))
	utils.AddFileFlagIfUpsert(listCmd.Flags(), "GET", ``)
	
	utils.AddPaginateFlagsIfListingResponse(listCmd.Flags(), "GET", `{
  &quot;description&quot; : &quot;successful operation&quot;,
  &quot;schema&quot; : {
    &quot;$ref&quot; : &quot;#/definitions/SWAGGER_OVERRIDE_list&quot;
  }
}`)
	speechandtextanalytics_programs_unpublishedCmd.AddCommand(listCmd)
	
	return speechandtextanalytics_programs_unpublishedCmd
}

var listCmd = &cobra.Command{
	Use:   "list",
	Short: "Get the list of Speech and Text Analytics unpublished programs",
	Long:  "Get the list of Speech and Text Analytics unpublished programs",
	Args:  utils.DetermineArgs([]string{ }),

	Run: func(cmd *cobra.Command, args []string) {
		queryParams := make(map[string]string)

		path := "/api/v2/speechandtextanalytics/programs/unpublished"

		nextPage := utils.GetFlag(cmd.Flags(), "string", "nextPage")
		if nextPage != "" {
			queryParams["nextPage"] = nextPage
		}
		pageSize := utils.GetFlag(cmd.Flags(), "int", "pageSize")
		if pageSize != "" {
			queryParams["pageSize"] = pageSize
		}
		urlString := path
		if len(queryParams) > 0 {
			urlString = fmt.Sprintf("%v?", path)
			for k, v := range queryParams {
				urlString += fmt.Sprintf("%v=%v&", url.QueryEscape(strings.TrimSpace(k)), url.QueryEscape(strings.TrimSpace(v)))
			}
			urlString = strings.TrimSuffix(urlString, "&")
		}

		retryFunc := CommandService.DetermineAction("GET", urlString, cmd.Flags())
		// TODO read from config file
		retryConfig := &retry.RetryConfiguration{
			RetryWaitMin: 5 * time.Second,
			RetryWaitMax: 60 * time.Second,
			RetryMax:     20,
		}
		results, err := retryFunc(retryConfig)
		if err != nil {
			logger.Fatal(err)
		}

		utils.Render(results)
	},
}
