		if printReqBody {
			
			reqModel := models.Knowledgeimportjobrequest{}
			utils.Render(reqModel.String())
			
			return
		}

		queryParams := make(map[string]string)

		path := "/api/v2/knowledge/knowledgebases/{knowledgeBaseId}/import/jobs"
		knowledgeBaseId, args := args[0], args[1:]
