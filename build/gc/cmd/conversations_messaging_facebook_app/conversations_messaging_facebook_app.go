var (
	Description = utils.FormatUsageDescription("conversations_messaging_facebook_app", "SWAGGER_OVERRIDE_/api/v2/conversations/messaging/facebook/app", )
	conversations_messaging_facebook_appCmd = &cobra.Command{
		Use:   utils.FormatUsageDescription("conversations_messaging_facebook_app"),
		Short: Description,
		Long:  Description,
	}
	CommandService services.CommandService
)

func init() {
	CommandService = services.NewCommandService(conversations_messaging_facebook_appCmd)
}

func Cmdconversations_messaging_facebook_app() *cobra.Command { 
	getCmd.SetUsageTemplate(fmt.Sprintf("%s\nOperation:\n  %s %s\n%s\n%s", getCmd.UsageTemplate(), "GET", "/api/v2/conversations/messaging/facebook/app", utils.FormatPermissions([]string{ "messaging:integration:add",  }), utils.GenerateDevCentreLink("GET", "Conversations", "/api/v2/conversations/messaging/facebook/app")))
	utils.AddFileFlagIfUpsert(getCmd.Flags(), "GET", ``)
	
	utils.AddPaginateFlagsIfListingResponse(getCmd.Flags(), "GET", `{
  "description" : "successful operation",
  "schema" : {
    "$ref" : "#/definitions/FacebookAppCredentials"
  }
}`)
	conversations_messaging_facebook_appCmd.AddCommand(getCmd)
	
	return conversations_messaging_facebook_appCmd
}

var getCmd = &cobra.Command{
	Use:   "get",
	Short: "Get Genesys Facebook App Id",
	Long:  "Get Genesys Facebook App Id",
	Args:  utils.DetermineArgs([]string{ }),

	Run: func(cmd *cobra.Command, args []string) {
		_ = models.Entities{}

		printReqBody, _ := cmd.Flags().GetBool("printrequestbody")
		if printReqBody {
			
			return
		}

		queryParams := make(map[string]string)

		path := "/api/v2/conversations/messaging/facebook/app"


		urlString := path
		if len(queryParams) > 0 {
			urlString = fmt.Sprintf("%v?", path)
			for k, v := range queryParams {
				urlString += fmt.Sprintf("%v=%v&", url.QueryEscape(strings.TrimSpace(k)), url.QueryEscape(strings.TrimSpace(v)))
			}
			urlString = strings.TrimSuffix(urlString, "&")
		}

		retryFunc := CommandService.DetermineAction("GET", urlString, cmd.Flags())
		// TODO read from config file
		retryConfig := &retry.RetryConfiguration{
			RetryWaitMin: 5 * time.Second,
			RetryWaitMax: 60 * time.Second,
			RetryMax:     20,
		}
		results, err := retryFunc(retryConfig)
		if err != nil {
			logger.Fatal(err)
		}

		utils.Render(results)
	},
}
